            switch (model)
            {
                case 0: return GetRulesModel0();
                case 1: return GetRulesModel1();
                case 2: return GetRulesModel2();
                case 3: return GetRulesModel3();
                case -1: return GetRulesModelNeg1();
                case -2: return GetRulesModelNeg2();
                case -3: return GetRulesModelNeg3();
                default: return GetRulesModel0();
            }
        }

